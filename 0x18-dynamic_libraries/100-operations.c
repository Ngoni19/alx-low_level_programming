/**
*add - The function adds two integers
*@x: first integer
*@y: second integer
*Return: x + y
*/
int add(int x, int y)
{
	return (x + y);
}
/**
 *sub - The function subtracts two integers
 *@x: first integer
 *@y: second integer
 *Return: result
 */
int sub(int x, int y)
{
	return (x - y);
}
/**
 *div - The function divides two integers
 *@x: first integer
 *@y: second integer
 *Return: result
 */
int div(int x, int y)
{
	return (x / y);
}
/**
 *mul - The function multiplies two integers
 *@x: first integer
 *@y: second integer
 *Return: x * y
 */
int mul(int x, int y)
{
	return (x * y);
}
/**
 *mod - The function finds mod of two integers
 *@x: first integer
 *@y: second integer
 *Return: x % y
 */
int mod(int x, int y)
{
	return (x % y);
}
